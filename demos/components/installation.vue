# 安装

## 通过npm安装

```bash
npm install xcui
```

## CDN


通过[unpkg.com/xcui/](https://unpkg.com/xcui/)可以获取最新版本的资源。


```html
    <!-- 引入样式 -->
    <link rel="stylesheet" href="https://unpkg.com/xcui/dist/xcui.css">
    <!-- 引入组件库 -->
    <script type="text/javascript" src="https://unpkg.com/xcui/dist/xcui.js"></script>

```



